package day01;

public class EX02 {
    public static void main(String[] args) {
        // num 변수의 값이 9면 A
        // 8이면 B
        // 7이면 C
        // 6이하면 F

        int num = 9;
        switch (num){
            case 9:
                System.out.println("A");
                break;
            case 8:
                System.out.println("B");
                break;
            case 7:
                System.out.println("C");
                break;
            default:
                System.out.println("F");

        }


        // if -> switch
//        int age = 19;
//        switch ()
    }
}
